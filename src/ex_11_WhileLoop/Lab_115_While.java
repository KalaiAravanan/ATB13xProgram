package ex_11_WhileLoop;

public class Lab_115_While {
    public static void main(String[] args) {
              int i = 0;
            while (i < 10) {
                System.out.println(i);
                i++;
            }

            //  I, C, U
            int j = 0;
            for (; j < 10; j++) {
                System.out.println(j);
            }
        }
    }

